let human = {
    'skin color': ['black', 'white','normal'], // 'skin color' have special character "Blank space" that's why we use single Quotes.
    hands: 2,
    legs: 2
};


// 'human' is object's name.
// inside object (human) we have properties with "Key-Value" pairs separated by colon ( ':' ).
// left side is Keys ('skin color' , hands , legs) could be any name.
// then we have colon ":" to separate keys with values like we are assigning values to keys.
// right side is values.
// If you want to add more than one property then you need to add comma.(except in the last property)